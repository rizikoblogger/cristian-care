module.exports = {


  friendlyName: 'Save forumanswer',


  description: '',


  inputs: {

    id: {
      type: 'string'
    },

    //  ╔═╗╦═╗╦╔╦╗╦╔╦╗╦╦  ╦╔═╗╔═╗
    //  ╠═╝╠╦╝║║║║║ ║ ║╚╗╔╝║╣ ╚═╗
    //  ╩  ╩╚═╩╩ ╩╩ ╩ ╩ ╚╝ ╚═╝╚═╝
    text: {
      type: 'string',
      required: true,
    },

    isModerated: {
      type: 'boolean'
    },

    //  ╔═╗╔╦╗╔╗ ╔═╗╔╦╗╔═╗
    //  ║╣ ║║║╠╩╗║╣  ║║╚═╗
    //  ╚═╝╩ ╩╚═╝╚═╝═╩╝╚═╝


    //  ╔═╗╔═╗╔═╗╔═╗╔═╗╦╔═╗╔╦╗╦╔═╗╔╗╔╔═╗
    //  ╠═╣╚═╗╚═╗║ ║║  ║╠═╣ ║ ║║ ║║║║╚═╗
    //  ╩ ╩╚═╝╚═╝╚═╝╚═╝╩╩ ╩ ╩ ╩╚═╝╝╚╝╚═╝

    forumquestion: {
      type: `string`,
      description: 'the ID of the question'
    },


  },


  exits: {},


  fn: async function (inputs) {

    const Forumanswer = sails.models.forumanswer;

    if (inputs.id) {
      const forumanswer = await Forumanswer.updateOne({id: inputs.id}).set({
        text: inputs.text,
        isModerated: inputs.isModerated
      });
      return {forumanswer: forumanswer};
    } else {
      const forumanswer = await Forumanswer.create({
        //  ╔═╗╦═╗╦╔╦╗╦╔╦╗╦╦  ╦╔═╗╔═╗
        //  ╠═╝╠╦╝║║║║║ ║ ║╚╗╔╝║╣ ╚═╗
        //  ╩  ╩╚═╩╩ ╩╩ ╩ ╩ ╚╝ ╚═╝╚═╝
        text: inputs.text,

        isModerated: false,

        //  ╔═╗╔═╗╔═╗╔═╗╔═╗╦╔═╗╔╦╗╦╔═╗╔╗╔╔═╗
        //  ╠═╣╚═╗╚═╗║ ║║  ║╠═╣ ║ ║║ ║║║║╚═╗
        //  ╩ ╩╚═╝╚═╝╚═╝╚═╝╩╩ ╩ ╩ ╩╚═╝╝╚╝╚═╝

        forumquestion: inputs.forumquestion,

        creator: this.req.me.id,
      }).fetch()
      return {forumanswer: forumanswer};
    }

  }


};
